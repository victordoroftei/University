#include <stdio.h>
#include <stdlib.h>

int main() {
    int a, b;

    scanf("%d", &a);
    printf("\n");
    scanf("%d", &b);
    printf("\n");

    int c, d, e;

    c = a + b;
    d = a - b;
    e = a * b;

    printf("%d", c);
    printf("\n");

    printf("%d", d);
    printf("\n");

    printf("%d", e);
    printf("\n");

    return 0;
}